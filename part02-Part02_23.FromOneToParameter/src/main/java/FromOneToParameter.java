

public class FromOneToParameter {

    public static void main(String[] args) {
        
        printUntilNumber(5);
        
    }
    public static void printUntilNumber(int number) {
            
        int z = 0;
            
        while (number > z) {
            z++;
            System.out.println(z);
        }
            

    

    }

}
